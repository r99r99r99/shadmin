package com.sdocean.firstpage.model;

public class MetaData4FirstPage {
	private String indicatorId;
	private String indicatorCode;
	private String indicatorTitle;
	private double mdata;
	private String unitName;
	private String unitDescription;
	private String unitLogo;
	public String getIndicatorId() {
		return indicatorId;
	}
	public void setIndicatorId(String indicatorId) {
		this.indicatorId = indicatorId;
	}
	public String getIndicatorCode() {
		return indicatorCode;
	}
	public void setIndicatorCode(String indicatorCode) {
		this.indicatorCode = indicatorCode;
	}
	public String getIndicatorTitle() {
		return indicatorTitle;
	}
	public void setIndicatorTitle(String indicatorTitle) {
		this.indicatorTitle = indicatorTitle;
	}
	public double getMdata() {
		return mdata;
	}
	public void setMdata(double mdata) {
		this.mdata = mdata;
	}
	public String getUnitName() {
		return unitName;
	}
	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}
	public String getUnitDescription() {
		return unitDescription;
	}
	public void setUnitDescription(String unitDescription) {
		this.unitDescription = unitDescription;
	}
	public String getUnitLogo() {
		return unitLogo;
	}
	public void setUnitLogo(String unitLogo) {
		this.unitLogo = unitLogo;
	}
}
